package exercise01;

public class PowTable {
	public static void main(String[] args) {
		System.out.println("a          b          pow(a,b)");
		int a=1;
		int b=2;
		System.out.println(a + "          " + b + "          " + (int) Math.pow(a,b));
		a=2;
		b=3;
		System.out.println(a + "          " + b + "          " + (int) Math.pow(a,b));
		a=3;
		b=4;
		System.out.println(a + "          " + b + "          " + (int) Math.pow(a,b));
	}

}
